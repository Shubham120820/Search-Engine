package com.Accio;
public class HistoryResult {
    // Member variables to store keyword and link
    String keyword;
    String link;

    // Getter method to retrieve the keyword
    public String getKeyword() {
        return keyword;
    }

    // Setter method to set the keyword
    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    // Getter method to retrieve the link
    public String getLink() {
        return link;
    }

    // Setter method to set the link
    public void setLink(String link) {
        this.link = link;
    }
}